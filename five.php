			<div class=portfolio_information>
				<p>Five’s hub replaces the conventional router, breaking away from the limitations of existing physical broadband infrastructure. The move to 5G brings interchangeable SIM cards; allowing flexibility of network providers for the user. In addition, the absence of a phone line allows freedom of positioning around the home. LED lighting allows visual diagnostic feedback.</p>

				<p>The mesh networking wall plug helps users to increase Wi-Fi coverage across their home. Aimed at users within larger/older homes, where Wi-Fi blind spots may occur. Removal of the safety cap (protecting infants from electrical shock) creates an available socket for other devices. The integrated Ethernet port allows “Power-Line” connectivity indirectly with the hub.</p>

				<p>A companion mobile application seamlessly guides the user through setting up their home network. The intuitive interface allows the user to; monitor network speeds, solve issues via live chat, manage devices accessing the internal network and wirelessly upgrade the hubs firmware. Furthermore, push notifications deliver live feedback on the status of five.</p>

